
public class Sandwich {

		//Declare variables for sandwich
		static String breadType;
		static String ingredient;
		static double price;
	
		//Returns declarations for sandwich
		public String getBreadType()	{
			return breadType;
		}
		public String getIngredient()	{
			return ingredient;
		}		
		public double getPrice()	{
			return price;
		}

		//Set new values of the above declarations
		public void setBreadType(String newBreadType)	{
			breadType = newBreadType;
		}		
		public void setIngredient(String newIngredient)	{
			ingredient = newIngredient;
		}		
		public void setPrice(int newPrice)	{
			price = newPrice;
		}
		
	}
	


